package bitwise_math;

public class Main {
    public static void main(String[] args) {
        int out = 2 << 8;
        System.out.println(out);

        out = 2 >> 1;
        System.out.println(out);

        out = 2 >> 2;
        System.out.println(out);

        out = 2 >> 2;
        System.out.println(out);

        out = 6 << 4;
        System.out.println(out);

        out = 23 & 8;
        System.out.println(out);

        out = 26 | 5;
        System.out.println(out);
    }
}
